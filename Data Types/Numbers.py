a = 10
print(a*10)  # Multiply by 10
print(a + 5)  # Add 5
print(a - 3)  # Subtract 3
print(a / 2)  # Divide by 2
print(a % 3)  # Modulus (remainder of division by 3)
print(a ** 2)  # Exponentiation (a squared)
print(a // 3)  # Floor division (integer division)
print(abs(a))  # Absolute value
print(round(a / 3, 2))  # Round to 2 decimal places
